    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        JComboBox<String> combo= (JComboBox<String>) evt.getSource();
        String selectedmodel = (String) combo.getSelectedItem();
        if(selectedmodel.equals("New Worker"))
        {
            new baddworker().show();
            this.hide();
        }
        if(selectedmodel.equals("Update profile"))
        {
            new bupdateworker().show();
            this.hide();
        }
        if(selectedmodel.equals("Delete profile"))
        {
            new bdeleteworker().show();
            this.hide();
        }
        
         if(selectedmodel.equals("Show Profiles"))
        {
            new ballworkerprofiles().show();
            this.hide();
        }
         
    }//GEN-LAST:event_jComboBox1ActionPerformed

