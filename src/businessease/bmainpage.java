    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        // TODO add your handling code here:
        JComboBox<String> combo= (JComboBox<String>) evt.getSource();
        String selectedmodel = (String) combo.getSelectedItem();
        if(selectedmodel.equals("New Assignment"))
        {
            new bnewassign().show();
            this.hide();
        }
        if(selectedmodel.equals("Pending Assignments"))
        {
            new bpendassign().show();
            this.hide();
        }
        if(selectedmodel.equals("Completed Assignments"))
        {
            new bcompassign().show();
            this.hide();
        }
        if(selectedmodel.equals("All Assignments"))
        {
            new ballassign().show();
            this.hide();
        }
    }//GEN-LAST:event_jComboBox2ActionPerformed

